public class Chacarters {
    public static void main(String[] args){
        char sex = 'M';
        System.out.println("\nSou do sexo " + sex + "asculino");

        //Can use ASCII values if you are familiar with them
        char myVar1 = 65, myVar2 = 66, myVar3 = 67;
        System.out.println("\nCaracteres ASCII\n");
        System.out.println("65 = " + myVar1);
        System.out.println("66 = " + myVar2);
        System.out.println("67 = " + myVar3);

        //Strings are sequence os characters
        String theEnd = "Fim do algoritmo!";
        System.out.println("\n" + theEnd);
    }
}
