public class OutputNumbers{
    public static void main(String[] args){
        //Java Output Numbers
        //Print Numbers
        System.out.println(3);
        System.out.println(358);
        System.out.println(50000);
        //Perform mathematical calculations
        int soma = 3 + 3;
        int multiplication = 3 * 3;
        int subtration = 3 - 3;
        float division = 3 / 3;
        System.out.println("3 + 3 = " + soma);
        System.out.println("3 x 3 = " + multiplication);
        System.out.println("3 - 3 = " + subtration);
        System.out.println("3 : 3 = " + division);

    }
}